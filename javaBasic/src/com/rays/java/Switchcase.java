package com.rays.java;

public class Switchcase {

	public static void main(String[] args) {
	
		String day = "monday";
		
		switch (day ) {  
		case "monday":
			System.out.println( "this is the first day of week");
			break;
		case "tuesday":
			System.out.println( "this is the second day of week");
		break;
		case "wednesday":
			System.out.println("this is the third day of week");
		case "thrusday":
			System.out.println( "this is the forth day of week");
			break;
		case "friday":
			System.out.println( "this is the fifth day of week");
			break;
		case " saturday":
			break;
		case "sunday":
			System.out.println( "this is the sixth day of week");
		default:
			System.out.println(" this day yet to come ");
			break;
			
			
		
		
		
		
		
		
		
		
		
		
		}
		
		
		
		

	}

}
